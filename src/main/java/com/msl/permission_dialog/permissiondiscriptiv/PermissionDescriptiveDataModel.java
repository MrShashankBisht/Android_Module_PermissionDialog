package com.msl.permission_dialog.permissiondiscriptiv;

public class PermissionDescriptiveDataModel {
    private String permissionName;
    private String permissionDescription;
    private String permissionDrawableName;

    public String getPermissionName() {
        return permissionName;
    }

    public void setPermissionName(String permissionName) {
        this.permissionName = permissionName;
    }

    public String getPermissionDescription() {
        return permissionDescription;
    }

    public void setPermissionDescription(String permissionDescription) {
        this.permissionDescription = permissionDescription;
    }

    public String getPermissionDrawableName() {
        return permissionDrawableName;
    }

    public void setPermissionDrawableName(String permissionDrawableName) {
        this.permissionDrawableName = permissionDrawableName;
    }
}
